         Términos y Condiciones de la Inscripción en Visitkorea.or.kr (miembros individuales y grupales)
<p>
1. Objeto de las Condiciones:
a. Los presentes Términos y Condiciones tienen por objeto el estipular todas y cada una de las cuestiones necesarias para que la Organización de Turismo de Corea (KTO) administre Visitkorea.or.kr (en adelante VisitKorea), su página web en idiomas extranjeros, cuyo propósito es promover el turismo de Corea.
b. VisitKorea proporcionará todos sus servicios a los usuarios, siempre y cuando consientan con los presentes Términos y Condiciones, y la página se administrará de acuerdo con tales condiciones reguladoras.
c. Los detalles de estos Términos y Condiciones pueden ser actualizados, en cualquier momento, y estar sujetos a tales cambios sin previo aviso a los usuarios.
</p>
 <p>   
Todas las condiciones actualizadas podrán encontrarse en VisitKorea.
 </p>
 <p>
2. Definición de usuario:
a. Los usuarios de VisitKorea se componen de miembros y no miembros y se refieren a todas y cada una de las personas que obtengan y utilicen información de VisitKorea.
b. Los miembros de VisitKorea están clasificados como miembros individuales o grupales, los cuales, a su vez, se subclasifican como miembros individuales Coreanos o extranjeros o miembros grupales Coreanos o extranjeros.
 </p>
3. Otras regulaciones aplicables:
a. Los presentes Términos y Condiciones estarán sujetos a la Ley de Comercio de Telecomunicaciones, a otras leyes aplicables, y a las prácticas comerciales estándar de la República de Corea.
4. Miembros:
a. Cualquier individuo y grupo que desee convertirse en miembro de VisitKorea puede obtener su inscripción cubriendo y presentando “online” los formularios de inscripción. 
b. VisitKorea otorgará la inscripción a los individuos y grupos que presenten los antedichos formularios de inscripción, excepto en los casos siguientes: 
I) Cuando haya cualquier información falsa u omitida, o cuando los individuos y grupos hayan sido clasificados incorrectamente, y 
II) Cuando los Términos y Condiciones sean incumplidos.
c. Cualquier cambio en la información de los miembros individuales y grupales deberá ser realizada, sin demora, en el perfil de miembro de VisitKkorea. Los miembros afectados serán, entera y exclusivamente, responsables de cualquier posible pérdida o daño, causado por no haber notificado el cambio de sus datos personales.
<p>
5.  Cancelación y supresión de la calidad de miembro:
a.	Los miembros podrán darse de baja, en cualquier momento, por medio de la modificación de sus datos. VisitKorea se asegurará de que las cancelaciones de afiliación sean completadas puntualmente, sin ninguna dificultad. 
b. Los miembros que deseen cancelar su afiliación con VisitKorea pueden recobrar tal afiliación en cualquier momento, a través del consiguiente proceso de registro de inscripción.
c. En cualquiera de los casos siguientes, VisitKorea puede limitar el acceso de sus miembros o rescindir su afiliación:
I) Cuando un miembro menosprecie a un país, individuo o grupo específico, o entable cualquier acto que pueda causar fricciones diplomáticas; 
II) Cuando un miembro inscriba información falsa al registrarse como miembro o al modificar la información de miembro; 
III) Cuando un miembro viole cualquier ley o condición como las aquí indicadas, o en caso de no ajustarse al sentido común;
IV) Cuando un miembro realice un cambio no autorizado en la información contenida en VisitKorea;
V) Cuando un miembro infringa los derechos humanos o los derechos de propiedad intelectual de una tercera parte y la información de la que es titular VisitKorea; 
VI) Cuando un miembro recopile y utilice ilegalmente la información de identificación de otros miembros;
VII) Cuando un miembro distribuya información, textos, o datos estadísticos de otros individuos o grupos (Coreanos o extranjeros) que sean de naturaleza delicada o violen prácticas generalmente aceptadas y la cortesía común: y 
VIII) Cuando un miembro cometa un acto criminal.
</p>
6. Política de Protección de Datos Concernientes a los Miembros Individuales y Grupales de VisiKorea:
a. VisiKorea protegerá estrictamente la información de todos los miembros individuales y grupales. 
<p>
7. Uso de la información:
a. VisitKorea ofrecerá libremente la información a los miembros.
b. VisitKorea incorporará contenidos de información turística (textos, imágenes, contenidos multimedia, fotografías en movimiento, etc.), tablas informativas, comunidades, foros, secciones de reservas, información para negocios, etc.
c. VisitKorea no mantendrá la autoridad para censurar la página web, pero se reserva el derecho de actualizar o borrar los contenidos de acuerdo con sus principios internos. VisitKorea puede limitar a los usuarios, sin previo aviso, el uso de sus contenidos, en parte o en su totalidad, cuando exista una causa justificada.
</p>
8. Obligaciones de VisitKorea:
a. VisitKorea no incurrirá en ningún acto en violación de las leyes o de los Términos y Condiciones, y proporcionará servicios ininterrumpidos y fidedignos bajo dichos Términos y Condiciones.
b. VisitKorea no enviará ningún correo electrónico comercial a sus miembros sin su previo consentimiento.
c. VisitKorea asumirá la completa responsabilidad y proporcionará una compensación razonable por cualquier pérdida que cause intencionadamente.
<p>
9. Obligaciones de los Usuarios Individuales y Grupales:
a. Los usuarios consienten en que no incurrirán en ninguno de los siguientes actos:
I) Proporcionar información falsa cuando se registren como miembro o cuando modifiquen la información de miembro;
II) Realizar cualquier cambio no autorizado en los contenidos de VisitKorea; 
III) Infringir los derechos humanos o los derechos de propiedad intelectual de una tercera parte o de los servicios gratuitos o de pago ofrecidos por VisitKorea;
IV) Utilizar ilegalmente la ID de otro miembro;
V) Distribuir información perjudicial para el orden y la moral públicos en cualquier forma, incluyendo textos e imágenes;
VI) Cometer un acto criminal; y
VII) Actuar en violación de las leyes y de los Términos y Condiciones.
b. VisitKorea puede suspender o rescindir la condición de miembro de los miembros individuales y grupales que cometan cualquiera de los actos establecidos en el precedente párrafo “a”, y borrar la información de tales usuarios sin previo aviso, de acuerdo con sus regulaciones internas.
</p>
10. Obligaciones de los Miembros Individuales y Grupales Concernientes a sus IDs y Contraseñas:
a. Los miembros individuales y grupales asumirán la completa responsabilidad del mantenimiento de la confidencialidad de sus IDs y contraseñas.
b. No se prestarán a que cualquier tercera parte utilice sus IDs o contraseñas. 
c. Notificarán inmediatamente a VisitKorea cualquier fuga o uso no autorizado de sus IDs y contraseñas, y cumplirán plenamente la política de seguridad de VisitKorea.
<p>
11. Gestión de los Anunciantes:
a. Los miembros consienten que si cualquier miembro individual o grupal coloca un anuncio en VisitKorea, por medio de un anunciante específico, VisitKorea no asumirá ninguna responsabilidad por cualquier pérdida incurrida del mismo, debido a que la transacción comercial en cuestión es por y entre el miembro y el anunciante. 
</p>
12. Restricciones de Almacenamiento:
a. Los miembros consienten en que VisitKorea pueda limitar en “Mi Página” la capacidad de almacenamiento, el período efectivo de almacenaje de contenidos, etc. 
b. Los miembros consienten en que VisitKorea puede suspender o eliminar “Mi Página” que esté inactiva durante cierto período de tiempo.

13. Autoridad y Responsabilidad Concernientes a los Contenidos Expuestos:
a. VisitKorea conservará la autoridad sobre todos los contenidos expuestos en la página web, si los proveedores de contenidos no se oponen, o si específicamente hay consentimiento entre VisitKorea y los proveedores de contenidos, no obstante, siempre que la persona que exponga el contenido tenga los derechos y la responsabilidad de tal contenido, tanto si está proporcionado por una comunidad, tabla informativa, etc., y VisitKorea tendrá el derecho de usar tales contenidos expuestos. 
b. VisitKorea protegerá todos los contenidos expuestos y se opondrá al plagio de la página web.
c. Ningún miembro utilizará comercialmente contenidos expuestos, incluyendo información de tramitación o de ventas que el miembro adquiera mediante el uso de los servicios de VisitKorea; no obstante, siempre que cualquier miembro desee utilizar los contenidos expuestos para propósitos no comerciales, dicho miembro obtendrá para ese propósito el consentimiento de la Organización de Turismo de Corea (KTO).
d. VisitKorea protegerá los contenidos expuestos y se opondrá a cualquier uso no autorizado de tales contenidos expuestos.
14. Revisión de los Términos y Condiciones:
a. VisitKorea se reserva el derecho de revisar los Términos y Condiciones.

15. Otros:
a. Los Términos y Condiciones serán interpretados y cumplidos de acuerdo con las leyes de la República de Corea, sometiéndose las partes, para cualquier cuestión litigiosa, a los Juzgados y Tribunales de la República de Corea.

Los presentes Términos y Condiciones entrarán en vigor el día 3 de marzo de 2008.

